fruits = ['apple', 'banana', 'cherry', 'date', 'stawberry']

# Print the entire list
print("Fruits List:", fruits)

# Accessing each item using indexing
print("\nAccessing each item using indexing:")
print("Fruits[0]:", fruits[0])
print("Fruits[1]:", fruits[1])
print("Fruits[2]:", fruits[2])
print("Fruits[3]:", fruits[3])
print("Fruits[4]:", fruits[4])

# Accessing items from the end of the list
print("\nAccessing items from the end of the list:")
print("Fruits[-1]:", fruits[-1])
print("Fruits[-2]:", fruits[-2])
print("Fruits[-3]:", fruits[-3])
print("Fruits[-4]:", fruits[-4])
print("Fruits[-5]:", fruits[-5])

# Accessing a range of items
print("\nAccessing a range of items:")
print("Fruits[1:3]:", fruits[1:3])
print("Fruits[1:]:", fruits[1:])
print("Fruits[:3]:", fruits[:3])

# Accessing every other item
print("\nAccessing every other item:")
print("Fruits[::2]:", fruits[::2])
print("Fruits[1::2]:", fruits[1::2])

